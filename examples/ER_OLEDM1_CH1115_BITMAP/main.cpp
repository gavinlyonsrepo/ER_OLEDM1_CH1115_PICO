//==== Function Space ===
// Test 1 OledBitmap write direct to screen
void Test1(void)
{
  myOLED.OLEDBitmap(0, 0, MYOLEDWIDTH, MYOLEDHEIGHT, fullScreenBuffer);
  busy_wait_ms(5000);
  myOLED.OLEDFillScreen(0x00, 0); // Clears screen
  busy_wait_ms(2000);
}

// Test 2 Init buffer with full screen bitmap on startup
void Test2(void)
{
  CH1115MultiBuffer Whole_screen; // Declare a struct to point to  a buffer
  // Intialise that struct with buffer details (&struct,  buffer, w, h, x-offset,y-offset)
  myOLED.OLEDinitBufferStruct(&Whole_screen, fullScreenBuffer, MYOLEDWIDTH, MYOLEDHEIGHT, 0, 0);
  myOLED.ActiveBuffer = &Whole_screen;
  myOLED.OLEDupdate();
  busy_wait_ms(5000);
}

// test (3) Draw a vertically addressed bitmap to screen with drawBitmap method
void Test3(void)
{
  CH1115MultiBuffer MyStruct;
  myOLED.OLEDinitBufferStruct(&MyStruct, fullScreenBuffer, MYOLEDWIDTH, MYOLEDHEIGHT, 0, 0); // Intialise that struct (&struct,buffer,w,h,x,y)
  myOLED.ActiveBuffer = &MyStruct;
  myOLED.OLEDclearBuffer(); // Clear active buffer

  myOLED.setDrawBitmapAddr(true); // for Bitmap Data Vertical  addressed
  myOLED.drawBitmap(0, 0, smallBitmapVa, 20, 20, FOREGROUND, BACKGROUND);
  myOLED.drawBitmap(30, 20, smallBitmapVa, 20, 20, BACKGROUND, FOREGROUND);

  myOLED.OLEDupdate();
  busy_wait_ms(10000);
}

//   test  (4) Draw a Horizontally addressed bitmap to screen with drawBitmap method
void Test4(void)
{
  CH1115MultiBuffer MyStruct;
  myOLED.OLEDinitBufferStruct(&MyStruct, fullScreenBuffer, MYOLEDWIDTH, MYOLEDHEIGHT, 0, 0); // Intialise that struct (&struct,buffer,w,h,x,y)
  myOLED.ActiveBuffer = &MyStruct;
  myOLED.OLEDclearBuffer(); // Clear active buffer

  myOLED.setDrawBitmapAddr(false); // for Bitmap Data Horziontal addressed
  myOLED.drawBitmap(40, 10, smallBitmapHa, 20, 20, FOREGROUND, BACKGROUND);
  myOLED.drawBitmap(100, 20, smallBitmapHa, 20, 20, BACKGROUND, FOREGROUND);

  myOLED.OLEDupdate();
  busy_wait_ms(10000);
}

void ClearScreen(void)
{
  myOLED.OLEDclearBuffer(); 
  myOLED.OLEDupdate();
  busy_wait_ms(2000);
}
